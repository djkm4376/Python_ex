A, B, C, D = 2, 7, 3, 17

print(A + B)    # 9
print(B % C)    # 1
print(A % B - C)    # -1
print(D // B * A)   # 4
print((A + B) * D // A) # 76
print(B ** A % A * C)   # 3
print(D / (A ** (A ** A + 1)) + 1)  # 1.53125
print(C % (A * B) + D)  # 20
print(C * (A % A) // A) # 0
print(D // (C ** C - 5))    # 0